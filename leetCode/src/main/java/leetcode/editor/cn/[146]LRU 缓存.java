class LRUCache {
    private int capacity;
    private Node first, end;
    public HashMap<Integer, Node> map;

    class Node {
        public int val;
        public Node prv;
        public Node nex;
    }

    public LRUCache(int capacity) {
        map = new HashMap<Integer, Node>(capacity);
        first = new Node();
        end = new Node();
        first.nex = end;
        first.prv = end;
        end.prv = first;
        end.nex = first;
    }

    public int get(int key) {
        if (!map.containsKey(key)) {
            return -1;
        }
        int value = map.get(key).val;
        // å°†nodeæ”¾åœ¨å‰é¢
        put(key, value);
    }

    // è¿™ä¸ªå¯ä¸å†™
    public void put(int key, int value) {
        if (map.containsKey(key)) {
            addFirst(key);
        } else {
            // å°äºå®¹é‡çš„æ”¾åœ¨æœ€å‰é¢
            Node node = new Node();
            node.val = value;
            if (map.size() < capacity) {
                map.put(key, node);
                addFirst(key);
            } else {
                delEnd(end.prv);
                map.put(key, node);
                addFirst(key);
            }

        }

    }

    public void delEnd(Node node) {
        node.prv.nex = node.nex;
        node.nex.pre = node.prv;
    }

    private void addFirst(int key) {
        Node n = map.get(key);
        Node next = first.nex;
        first.nex = n;
        n.prev = first;
        n.next = next;
        next.prev = n;
    }


}


